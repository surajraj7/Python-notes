name="suraj"

# len()
print(len(name))

# string.endswith("rry")
print(name.endswith("raj"))

# string.startswith("rry")
print(name.startswith("raj"))

# capitalize
print(name.capitalize())

# string.replace
a="suraj is a good good boy"
print(a.replace("good","bad"))

text = "hello world"
print(text.upper())      # HELLO WORLD
print(text.lower())      # hello world
print(text.title())      # Hello World
print(text.capitalize()) # Hello world

x = "Python"
print(x.strip())   # "Python"
print(x.lstrip())  # "Python   "
print(x.rstrip())  # "   Python"


